SELECT
    SURNAME,
    EMP_ID,
    ( SUBSTR(
        SURNAME, 1, 2
    )
      || EMP_ID ) AS LOGIN
FROM
    EMPLOYEES
2.
SELECT
    SURNAME
FROM
    EMPLOYEES
WHERE
    INSTR(
        SURNAME, 'L'
    ) > 0
    OR INSTR(
        SURNAME, 'l'
    ) > 0
3.
SELECT
    SURNAME
FROM
    EMPLOYEES
WHERE
    INSTR(
        SUBSTR(SURNAME,1, LENGTH(SURNAME)/2), 'L'
    ) > 0
    OR INSTR(
        SUBSTR(SURNAME,1, LENGTH(SURNAME)/2), 'l'
    ) > 0
4.
SELECT
    SURNAME,
    SALARY                     AS ORIGINAL_SALARY,
    ROUND(SALARY * 1.15)       AS INCREASED_SALARY
FROM
    EMPLOYEES
5.
SELECT
    TO_CHAR(
        CURRENT_DATE, 'DAY'
    ) AS TODAY
FROM
    EMPLOYEES
WHERE
    ROWNUM = 1
6.
SELECT
    TO_CHAR(
        DATE '2000-12-13', 'DAY'
    ) AS TODAY
FROM
    EMPLOYEES
WHERE
    ROWNUM = 1
