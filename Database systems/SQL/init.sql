CREATE TABLE DEPARTMENTS(
	DEPT_ID NUMERIC(2) CONSTRAINT PK_DEPARTMENT PRIMARY KEY,
	DEPT_NAME  VARCHAR(20),
	ADDRESS VARCHAR(20) DEFAULT 'not set');

CREATE TABLE JOBS(
	NAME VARCHAR(20) CONSTRAINT PK_JOB PRIMARY KEY,
	MIN_SALARY NUMERIC(6,2),
	MAX_SALARY NUMERIC(6,2));

CREATE TABLE EMPLOYEES(
	EMP_ID NUMERIC(4) CONSTRAINT PK_EMPLOYEE PRIMARY KEY,
	SURNAME VARCHAR(15),
	NAME VARCHAR(15),
	JOB VARCHAR(20) CONSTRAINT FK_JOB REFERENCES JOBS(NAME),
	BOSS_ID NUMERIC(4) CONSTRAINT FK_BOSS REFERENCES EMPLOYEES(EMP_ID), 
	HIRE_DATE DATE,
	SALARY NUMERIC(6,2) CONSTRAINT MIN_SALARY CHECK(SALARY>100),
	ADD_SALARY NUMERIC(6,2),
	DEPT_ID NUMERIC(2) CONSTRAINT FK_DEPARTMENT REFERENCES DEPARTMENTS(DEPT_ID));
  
